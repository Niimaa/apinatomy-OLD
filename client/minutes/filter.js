'use strict';

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
define(['app/module', 'lodash'], function (app, _) {
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	app.filter('minutes', function () {
		return function (input) {
			input = _(input).parseInt();

			var s = input / 1000 % 60;
			var m = input % (60 * 1000);

			return m + ":" + s;
		};
	});

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
});/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
