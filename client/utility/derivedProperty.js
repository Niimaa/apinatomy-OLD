'use strict';

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
define(['lodash'], function () {
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


	_.mixin({ derivedProperty: function (object, prop, getter, setter) {
		Object.defineProperty(object, prop, {
			enumerable: true,
			get: getter,
			set: setter
		});

		return object;
	}}, {chain: true});


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
});/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
