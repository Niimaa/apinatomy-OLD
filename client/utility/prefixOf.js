'use strict';

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
define(['lodash'], function (_) {
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


	_.mixin({ prefixOf: function (A, B) {
		if (A.length > B.length) { return false; }
		for (var i = 0; i < A.length; ++i) {
			if (A[i] !== B[i]) { return false; }
		}
		return true;
	}}, {chain: false});


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
});/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
