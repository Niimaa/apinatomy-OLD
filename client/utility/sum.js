'use strict';

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
define(['lodash'], function (_) {
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


	function DEFAULT_PLUS_FN(a, b) { return a + b; }


	_.mixin({ sum: function (collection, plusFn) {
		return _.reduce(collection, plusFn || DEFAULT_PLUS_FN, 0)
	}}, {chain: true});


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
});/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
