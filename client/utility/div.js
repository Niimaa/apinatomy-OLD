'use strict';

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
define(['lodash'], function (_) {
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


	//// Number division with a naive treatment of Infinity

	_.mixin({
		div: function (a, b) {
			if (a === b) {
				return 1;
			} else if (b === 0) {
				return Infinity;
			} else {
				return a / b;
			}
		}
	}, {chain: false});


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
});/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
